ColorDepthManager::ColorDepthManager()
    : currentDepth_(Depth::Depth16)
    , paletteDirty_(true)
    , customPalette_(nullptr)
    , usingCustomPalette_(false)
    , totalBytesTransferred_(0)
    , frameCount_(0) {
}

void ColorDepthManager::setDepth(Depth depth) {
    if (currentDepth_ != depth) {
        currentDepth_ = depth;
        paletteDirty_ = true;
        // Note: palette rebuild would happen here in full implementation
        // For now, flag as dirty so caller knows to rebuild
    }
}

void ColorDepthManager::setCustomPalette(const uint16_t* palette256) {
    customPalette_ = palette256;
    usingCustomPalette_ = (palette256 != nullptr);
    paletteDirty_ = true;
}

const uint16_t* ColorDepthManager::getPalette() const {
    if (customPalette_ != nullptr) {
        return customPalette_;
    }
    return kDefault256ColorPalette;
}

